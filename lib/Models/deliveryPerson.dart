class DeliveryPerson {
  String id;
  String name;
  String phone;
  String address;
  String email;
  String imageUrl;
  bool isAvailable;

  DeliveryPerson({
    required this.id,
    required this.name,
    required this.phone,
    required this.address,
    required this.email,
    required this.imageUrl,
    required this.isAvailable,
  });
}
